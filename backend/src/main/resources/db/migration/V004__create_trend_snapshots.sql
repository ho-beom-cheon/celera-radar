CREATE TABLE IF NOT EXISTS trend_snapshots (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    keyword_id BIGINT NOT NULL,
    snapshot_date DATE NOT NULL,
    period_start DATE NOT NULL,
    period_end DATE NOT NULL,
    time_unit VARCHAR(20) NOT NULL,
    data_period DATE NOT NULL,
    ratio NUMERIC(10,4) NOT NULL,
    device VARCHAR(20) NOT NULL DEFAULT 'ALL',
    gender VARCHAR(20) NOT NULL DEFAULT 'ALL',
    ages VARCHAR(100) NOT NULL DEFAULT '',
    raw_payload JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_trend_snapshots_keyword FOREIGN KEY (keyword_id) REFERENCES keywords(id),
    CONSTRAINT ck_trend_snapshots_time_unit CHECK (time_unit IN ('DATE','WEEK','MONTH'))
);

