CREATE UNIQUE INDEX IF NOT EXISTS uk_trend_snapshot_keyword_date_period_time_unit_filter
    ON trend_snapshots (
        keyword_id,
        snapshot_date,
        time_unit,
        data_period,
        COALESCE(device, ''),
        COALESCE(gender, ''),
        COALESCE(ages, '')
    );
