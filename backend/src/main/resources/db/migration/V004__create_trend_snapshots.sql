    ON trend_snapshots(keyword_id, snapshot_date DESC);
CREATE INDEX IF NOT EXISTS idx_trend_snapshots_data_period
    ON trend_snapshots(data_period);

CREATE TABLE IF NOT EXISTS batch_job_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_type VARCHAR(50) NOT NULL,
    trigger_type VARCHAR(20) NOT NULL,
    status VARCHAR(20) NOT NULL,
    target_count INT NOT NULL DEFAULT 0,
    success_count INT NOT NULL DEFAULT 0,
    failure_count INT NOT NULL DEFAULT 0,
    started_at TIMESTAMPTZ NOT NULL,
    finished_at TIMESTAMPTZ,
    error_message VARCHAR(500),
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT ck_batch_job_history_job_type CHECK (job_type IN ('SHOPPING_SEARCH_DAILY','DATALAB_TREND_DAILY','ALERT_GENERATE_DAILY')),
    CONSTRAINT ck_batch_job_history_trigger_type CHECK (trigger_type IN ('MANUAL','SCHEDULED')),
    CONSTRAINT ck_batch_job_history_status CHECK (status IN ('RUNNING','SUCCESS','PARTIAL_SUCCESS','FAILED'))
);

CREATE INDEX IF NOT EXISTS idx_batch_job_history_started_at
    ON batch_job_history(started_at DESC);
CREATE INDEX IF NOT EXISTS idx_batch_job_history_job_type_status
    ON batch_job_history(job_type, status);
