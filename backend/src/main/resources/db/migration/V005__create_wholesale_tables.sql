UPDATE wholesale_products product
SET user_id = upload.user_id,
    source_name = COALESCE(product.source_name, upload.source_name)
FROM wholesale_uploads upload
WHERE product.upload_id = upload.id
  AND (product.user_id IS NULL OR product.source_name IS NULL);

UPDATE wholesale_products
SET is_sold_out = FALSE
WHERE is_sold_out IS NULL;

ALTER TABLE wholesale_products ALTER COLUMN user_id SET NOT NULL;
ALTER TABLE wholesale_products ALTER COLUMN is_sold_out SET NOT NULL;

