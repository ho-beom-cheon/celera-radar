CREATE TABLE IF NOT EXISTS wholesale_uploads (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    source_name VARCHAR(100),
    original_file_name VARCHAR(255) NOT NULL,
    file_path VARCHAR(1000) NOT NULL,
    file_size BIGINT NOT NULL DEFAULT 0,
    file_type VARCHAR(20) NOT NULL DEFAULT 'CSV',
    requested_encoding VARCHAR(20) NOT NULL DEFAULT 'AUTO',
    detected_encoding VARCHAR(20) NOT NULL DEFAULT 'UTF_8',
    total_rows INT NOT NULL DEFAULT 0,
    success_rows INT NOT NULL DEFAULT 0,
    failed_rows INT NOT NULL DEFAULT 0,
    detected_columns TEXT,
    mapping_product_name VARCHAR(255),
    mapping_supply_price VARCHAR(255),
    mapping_shipping_fee VARCHAR(255),
    mapping_category VARCHAR(255),
    mapping_product_url VARCHAR(255),
    column_mapping JSONB,
    status VARCHAR(30) NOT NULL DEFAULT 'UPLOADED',
    error_summary TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_wholesale_uploads_user FOREIGN KEY (user_id) REFERENCES users(id),
    CONSTRAINT ck_wholesale_uploads_status CHECK (status IN ('UPLOADED','MAPPED','PARSED','FAILED'))
);

DO $$
BEGIN
