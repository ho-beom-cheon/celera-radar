CREATE TABLE IF NOT EXISTS wholesale_products (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    upload_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    row_no INT NOT NULL,
    source_name VARCHAR(100),
    external_product_id VARCHAR(200),
    product_name TEXT,
    normalized_product_name TEXT,
    supply_price NUMERIC(14,2),
    shipping_fee NUMERIC(14,2),
    image_url TEXT,
    product_url TEXT,
    category VARCHAR(200),
    brand VARCHAR(200),
    maker VARCHAR(200),
    option_name VARCHAR(300),
    stock_status VARCHAR(100),
    is_sold_out BOOLEAN NOT NULL DEFAULT FALSE,
    memo TEXT,
    parse_status VARCHAR(20) NOT NULL DEFAULT 'PARSED',
    error_message TEXT,
    raw_row JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_wholesale_products_upload FOREIGN KEY (upload_id) REFERENCES wholesale_uploads(id),
    CONSTRAINT fk_wholesale_products_user FOREIGN KEY (user_id) REFERENCES users(id),
    CONSTRAINT ck_wholesale_products_parse_status CHECK (parse_status IN ('PARSED','INVALID'))
);

